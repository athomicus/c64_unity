// =============================================================
// DrawChar — rysuje pojedynczy znak na pozycji (cx, cy) w sieci komorek
// cx: kolumna 0-39, cy: wiersz 0-24
// fg/bg: kolory C64 (0-15)
// =============================================================
void DrawChar(unsigned char c, unsigned char cx, unsigned char cy,
              unsigned char fg, unsigned char bg)
{
    unsigned char row;
    unsigned char idx;
    unsigned int cell;
    unsigned char color;

    /* mapuj znak na indeks tablicy */
    if (c >= 'a' && c <= 'z')      idx = c - 'a';
    else if (c >= 'A' && c <= 'Z') idx = c - 'A';
    else                            idx = 26; /* spacja / nieznany */

    cell  = (unsigned int)cy * 40 + cx;
    color = (fg << 4) | (bg & 0x0F);

    for (row = 0; row < 8; row++)
        *((unsigned char*)0x6000 + cell * 8 + row) = c64_font[idx][row];

    *((unsigned char*)0x4400 + cell) = color;
}

// =============================================================
// DrawText — rysuje string na ekranie od pozycji (cx, cy)
// Automatycznie przechodzi do nowej linii gdy cx >= 40
// Przyklad: DrawText("hello", 2, 1, C64_WHITE, C64_BLACK);
// =============================================================
void DrawText(const char* str, unsigned char cx, unsigned char cy,
              unsigned char fg, unsigned char bg)
{
    unsigned char start_cx = cx;

    while (*str) {
        if (*str == '\n') {
            cx = start_cx;
            cy++;
        } else {
            if (cx >= 40) { cx = start_cx; cy++; }
            if (cy >= 25) break; /* poza ekranem */
            DrawChar(*str, cx, cy, fg, bg);
            cx++;
        }
        str++;
    }
}

